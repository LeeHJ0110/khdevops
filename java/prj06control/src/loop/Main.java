package loop;

public class Main {

	public static void main(String[] args) {
		/*
		 * 반복문
		 * 
		 * [while] : 조건식이 참이면 블럭 내용 실행
		 * while(조건식){
		 * 	실행내용
		 * }
		 * 
		 * [do while] : 일단 조건 없이 실행
		 * do{
		 *  실행내용
		 * }while(조건식);
		 * 
		 * 
		 * [for] : 초기식 조건식 증감식 한번에 작성
		 * for(초기식 ; 조건식 ; 증감식){
		 * 	실행내용
		 * }
		 * 
		 */
		for(int i = 0 ; i < 10 ; i++) {
			System.out.println("Hello");
		}
	}

}
