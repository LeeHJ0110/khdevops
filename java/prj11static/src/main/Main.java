package main;

public class Main {

	public static void main(String[] args) {
		/*
		 * static : 메모리 영역중 static 영역에 적재한다
		 * - 프로그램 실행 처음부터 끝까지 존재하는 영역 (자동으로 지워지지 않음)
		 * - 객체 생성과 무관하게 사용 가능
		 * - 클래스명.변수명
		 * - 클래스명.메서드명(...)
		 * 
		 * 
		 * [static]
		 * - static 키워드를 앞에 붙이면, static 변수 및 static 메서드를 만들 수 있다
		 * - static 메서드 내에서 객체의 맴버 참조할 수 없다
		 * 
		 * [객체의 생명주기]
		 * 객체는 Heap 메모리에 생성된다
		 * 참조가 끊어지면, 메모리에서 제거된다
		 * 
		 * [지역변수]
		 * 지역변수는 Stack 메모리에 적재된다.
		 * Stack 메모리는 다른 쓰레드와 공유가 불가능한 영역이다.
		 * 
		 * 
		 * 
		 */
		
		System.out.println("===== static =====");
		
		
	}

}
